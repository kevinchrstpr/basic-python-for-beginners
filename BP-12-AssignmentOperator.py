# Operasi yang dapat dilakukan dengan penyingkatan
# Operasi ditambah dengan assignment

a = 10 # ini merupakan assignment
print("nilai a = ",a)

a += 1 # a = a + 1
print("\nNilai a += 1 : ", a)

a -= 2 # a = a - 2
print("Nilai a -= 2 : ", a)

a /= 3 # a = a / 1
print("Nilai a /= 1 : ",a)

a *= 2 # a = a * 1
print("Nilai a *= 1 : ",a)

b = 10 # assignment kedua
print("\nNilai b = ",b)

b %= 4
print("Nilai b %= 4 : ",b)

b = 10 
print("\nNilai b = ",b)

b //= 3
print("Nilai b //= 3 : ",b)

b = 10
print("\nNilai b = ",b)

b **= 2
print("Nilai b = ",b)

