#all the data type that python have

# data type = integer
data_integer = 1
print("data : ", data_integer)
print("type : ", type(data_integer))

#data type = float (decimal)
data_float = 1.5
print("data : ", data_float)
print("type : ", type(data_float))

#data type = string (character)
data_string = "kevin"
print("data : ", data_string)
print("type : ", type(data_string))

#data type = boolean (binnary number, true/false)
data_bool = True
print("data : ", data_bool)
print("type : ", type(data_bool))

data_bool = False
print("data : ", data_bool)
print("type : ", type(data_bool))

##special data type for mathematics
# Complex number (imaginer number)
data_complex = complex(5,6)
print("data : ", data_complex)
print("type : ", type(data_complex))

# data type from C programming language

from ctypes import c_double

data_c_double = c_double(10.5)
print("data : ", data_c_double)
print("type : ", type(data_c_double))

